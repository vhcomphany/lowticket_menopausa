// Gera cardápio semanal baseado no perfil da usuária
export function generateWeeklyPlan(mainSymptom: string, planLevel: 'adaptativo' | 'intermediario' | 'avancado') {
  const days = ['Segunda', 'Terça', 'Quarta', 'Quinta', 'Sexta', 'Sábado', 'Domingo'];
  const symptom = (mainSymptom || 'fogacho') as Symptom;

  const getCategory = (cat: RecipeCategory, sym: Symptom) => {
    const bySymptom = RECIPES.filter(r => r.category === cat && r.symptoms.includes(sym));
    const fallback = RECIPES.filter(r => r.category === cat);
    const pool = bySymptom.length > 0 ? bySymptom : fallback;
    return pool[Math.floor(Math.random() * pool.length)];
  };

  return days.map((day, i) => ({
    day,
    cafe: getCategory('vitamina', symptom) || getCategory('suco', symptom),
    lanche_manha: getCategory('shot', symptom),
    almoco: getCategory('comida', symptom),
    lanche_tarde: getCategory('lanche', symptom),
    jantar: planLevel === 'avancado' ? getCategory('caldo', symptom) : getCategory('comida', symptom),
    cha: getCategory('cha', symptom),
  }));
}
